
# Determinar la edad de una persona conociendo el año actual y el año de nacimiento.

# ENTRADA DE DATOS
año_de_nacimiento = int(input("Escribe el año de nacimiento: "))


# PROCESO
año_actual = 2022
edad = año_actual - año_de_nacimiento

# SALIDA DE DATOS
print("La edad es =", edad)
print("La edad es = " + str(edad))
print(f"La edad es = {edad}")